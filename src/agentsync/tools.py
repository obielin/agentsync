def get_tool(tool_id: str) -> Tool:
    if tool_id not in ALL_TOOLS:
        raise KeyError(f"Unknown tool {tool_id!r}. Available: {sorted(ALL_TOOLS)}")
    return ALL_TOOLS[tool_id]


def get_tools(tool_ids: list[str]) -> list[Tool]:
    return [get_tool(t) for t in tool_ids]


